package JumpToJavaWithDuoen;
public class Sample {
  public static void main(String[] args) {
    int a = 10;
    int b = 5;
    String hello = "hello";
    System.out.println(hello);
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    System.out.println(a % b);
    System.out.println(a++);
    System.out.println(a);
  }
}
